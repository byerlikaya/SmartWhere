namespace SmartWhere.Enums
{
    public enum ComparisonOperator
    {
        /// <summary>
        /// "="	Equal to
        /// </summary>
        Equal,
        /// <summary>
        /// "<>" Not equal to
        /// </summary>
        NotEqual,
        /// <summary>
        /// ">" Greater than 
        /// </summary>
        GreaterThan,
        /// <summary>
        /// ">=" Greater than or equal to
        /// </summary>
        GreaterThanOrEqual,
        /// <summary>
        /// "<" Less than
        /// </summary>
        LessThan,
        /// <summary>
        /// "<=" Less than or equal to
        /// </summary>
        LessThanOrEqual
    }
}
